public class SimpleCalculator {
    public static void main(String[] args) {
        int num1 = 6;
        int num2 = 4;
        int num3 = num1 + num2;
        System.out.println("The result of addition is: " + num3);
        int num4 = 10;
        int num5 = 20;
        int num6 = num5 - num4;
        System.out.println("the result of subtraction is: " + num6);
        int num7 = 16;
        int num8 = 9;
        int num9 = num7 * num8;
        System.out.println("The result of multipilcation is: " + num9);
        int num10 = 50;
        int num11 = 5;
        int num12 = num10 / num11;
        System.out.println("The result of division is: " + num12);
        int M = 9;
        float S = 38.5F;
        System.out.println("The result of Modulus of M is: " + M % 10);
        System.out.println("The result of Modulus of S is: " + S % 10.0F);
    }
}
